package supportClasses;

public class StatuFeuRouge {
	
	public double nbVehicle = 0;
	public double waitingTime = 1.0;
	public double passingTime = 1.0;
	public double timer = 0;
	public double getNbVehicle() {
		return nbVehicle;
	}
	public void setNbVehicle(double nbVehicle) {
		this.nbVehicle = nbVehicle;
	}
	public double getWaitingTime() {
		return waitingTime;
	}
	public void setWaitingTime(double waitingTime) {
		this.waitingTime = waitingTime;
	}
	public double getPassingTime() {
		return passingTime;
	}
	public void setPassingTime(double passingTime) {
		this.passingTime = passingTime;
	}
	public double getTimer() {
		return timer;
	}
	public void setTimer(double timer) {
		this.timer = timer;
	}
	
}
